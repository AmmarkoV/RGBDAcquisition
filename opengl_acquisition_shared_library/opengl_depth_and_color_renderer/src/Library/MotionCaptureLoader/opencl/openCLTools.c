unsigned long tickBaseJointEstimatorOCL = 0;

unsigned long GetTickCountMicrosecondsOCL()
{
    struct timespec ts;
    if ( clock_gettime(CLOCK_MONOTONIC,&ts) != 0)
        {
            return 0;
        }

    if (tickBaseJointEstimatorOCL==0)
        {
            tickBaseJointEstimatorOCL = ts.tv_sec*1000000 + ts.tv_nsec/1000;
            return 0;
        }

    return ( ts.tv_sec*1000000 + ts.tv_nsec/1000 ) - tickBaseJointEstimatorOCL;
}



char * read_file(const char * filename,int *length )
{
    *length = 0;
    FILE * pFile = fopen ( filename, "rb" );

    if (pFile==0)
        {
            return 0;
        }

    // obtain file size:
    fseek (pFile, 0, SEEK_END);
    unsigned long lSize = ftell (pFile);
    rewind (pFile);

    // allocate memory to contain the whole file:
    unsigned long bufferSize = sizeof(char)*(lSize+1);
    char * buffer = (char*) malloc (bufferSize);
    if (buffer == 0 )
        {
            fclose(pFile);
            return 0;
        }

    // copy the file into the buffer:
    size_t result = fread (buffer,1,lSize,pFile);
    if (result != lSize)
        {
            free(buffer);
            fclose(pFile);
            return 0;
        }

    /* the whole file is now loaded in the memory buffer. */

    // terminate
    fclose (pFile);

    buffer[lSize]=0; //Null Terminate Buffer!
    *length = (unsigned int) lSize;
    return buffer;
}




